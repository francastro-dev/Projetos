--CRIANDO AS TABELAS DO BANCO DE DADOS

CREATE TABLE cliente(
id_cliente SMALLINT IDENTITY PRIMARY KEY,
nome VARCHAR(150) NOT NULL,
cpf VARCHAR(20) NOT NULL,
turnoTrabalhado VARCHAR(20) NOT NULL,
);

CREATE TABLE produto(
id_produto SMALLINT IDENTITY PRIMARY KEY,
nomeProduto VARCHAR(20) NOT NULL,
valorProduto FLOAT(5)
);

CREATE TABLE pedido(
id_pedido SMALLINT IDENTITY PRIMARY KEY,
id_cliente SMALLINT,
id_produto SMALLINT,
dataPedido DATETIME,
valorPedido FLOAT(5),
formadePagamento VARCHAR(20)

--CRIANDO AS CHAVES ESTRANGEIRAS
CONSTRAINT fk_pedido_cliente FOREIGN KEY(id_cliente)
REFERENCES cliente(id_cliente),

CONSTRAINT fk_pedido_produto FOREIGN KEY(id_produto)
REFERENCES pedido(id_pedido)
);

